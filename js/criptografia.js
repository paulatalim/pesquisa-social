function encrypt (text) {
  return text.replace(/e/g, "enter").replace(/i/g, "1dSe").replace(/a/g, "FDvg45").replace(/o/g, "Jk67vG").replace(/u/g, "Wi23AD").replace("@", "3901").replace(".", "57A9");
};

function decrypt (ciphertext) {
  return ciphertext.replace(/enter/g, "e").replace(/1dSe/g, "i").replace(/FDvg45/g, "a").replace(/Jk67vG/g, "o").replace(/Wi23AD/g, "u").replace(/3901/g, "@").replace(/57A9/g, ".");
};